function GetApartmentId(coords, gridSize)
    local zoneName = GetNameOfZone(coords.x, coords.y, coords.z)
    local zoneData = GtaZones[zoneName]
    
    if not zoneData then
        return math.random(1,99).."-"..math.random(1,99)
    end
    
    local minX, minY, maxX, maxY = math.huge, math.huge, -math.huge, -math.huge
    for _, point in ipairs(zoneData) do
        minX = math.min(minX, point.x)
        minY = math.min(minY, point.y)
        maxX = math.max(maxX, point.x)
        maxY = math.max(maxY, point.y)
    end
    
    local gridX = math.floor((coords.x - minX) / gridSize)
    local gridY = math.floor((coords.y - minY) / gridSize)
    return string.format("%d-%d", gridX, gridY)
end